-- Переработка «неопределённых» категорий-свалок, чтобы траты попадали в конкретную категорию.

-- «Маркетплейсы и подарки» → «Подарки».
-- Маркетплейс (Ozon/WB) — это КАНАЛ покупки, а не суть: покупки оттуда классифицируются
-- по сути товара (продукты, детские товары, товары для дома…), а не в общий бакет.
-- Категория остаётся только для подарков другим людям. Идемпотентно: при повторе WHERE не найдёт строку.
UPDATE categories
SET name = 'Подарки',
    emoji = '🎁',
    aliases = ARRAY['подарок', 'подарки', 'презент', 'сюрприз', 'подарок на день рождения', 'gift'],
    description = 'Только подарки и презенты другим людям. Покупки на маркетплейсах (Ozon, Wildberries, WB) НЕ сюда — их относят к категории по сути товара.'
WHERE name = 'Маркетплейсы и подарки';

-- «Другое» — только крайний fallback. Имя не меняем (на него завязан код парсера),
-- но задаём описание-предупреждение, чтобы AI не сваливал сюда «на отмахись».
UPDATE categories
SET description = 'Крайний случай: использовать ТОЛЬКО если трата не подходит ни к одной другой категории. Это не свалка — сначала ищи конкретную категорию.'
WHERE name = 'Другое';
